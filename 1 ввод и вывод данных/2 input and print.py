'''
Команда print() используется для вывода данных на экран. В круглых скобках указываются данные, которые нужно вывести (аргументы).
Если это текст, его нужно заключить в одинарные или двойные кавычки. Команда print() позволяет указывать несколько аргументов, 
в таком случае их надо отделять запятыми.

Аргументы – это конкретные значения, которые передаются функции при ее вызове.

Команда input() используется для считывания данных. Когда программа доходит до места, где есть input(), она ожидает, 
что пользователь введёт данные с клавиатуры и нажмёт Enter.
 
Переменная – место в памяти, куда сохраняются данные. Любая переменная имеет имя и значение.

Именование переменных:

- в имени переменной рекомендуется использовать только латинские буквы a-z, A-Z, цифры и символ нижнего подчеркивания (_)
- имя переменной не может начинаться с цифры
- имя переменной не может содержать пробелы
- имя переменной по возможности должно отражать ее назначение

Для именования переменных принято использовать стиль lower_case_with_underscores (слова из маленьких букв с подчеркиваниями).

Значение переменной — это сохранённая в ней информация: текст, число и т.д.

Символ = в языках программирования выступает в роли оператора присваивания. Он выполняет задачу связывания значения, расположенного 
справа от него, с именем переменной, указанной слева.
'''